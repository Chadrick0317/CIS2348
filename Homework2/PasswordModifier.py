word = input()

word = word.replace('i','!') #i becomes !
word = word.replace('a','@') #a becomes @
word = word.replace('m','M') #m becomes M
word = word.replace('B','8') #B becomes 8
word = word.replace('o','.') #o becomes .

word = word + 'q*s' #appending "q*s"

print(word)
